"""
Preprocessing transforms for Leaf-JEPA IRP.
Auto-generated by Stage 2 notebook 04_preprocessing.ipynb.

Normalisation stats computed from PlantVillage TRAINING set only.
Hue jitter capped at 0.05 across all pipelines to preserve disease colour signal.

Usage:
    from transforms import get_eval_transforms, get_finetune_transforms, get_pretrain_transforms
"""

from torchvision import transforms

NORM_MEAN = [0.466726, 0.488969, 0.41028]
NORM_STD  = [0.195034, 0.170282, 0.213409]
IMAGE_RESIZE = 256
IMAGE_CROP   = 224


def get_eval_transforms():
    """Deterministic evaluation pipeline — no randomness."""
    return transforms.Compose([
        transforms.Resize(IMAGE_RESIZE),
        transforms.CenterCrop(IMAGE_CROP),
        transforms.ToTensor(),
        transforms.Normalize(mean=NORM_MEAN, std=NORM_STD),
    ])


def get_finetune_transforms():
    """Standard training augmentation for supervised fine-tuning (Stages 3 & 5)."""
    return transforms.Compose([
        transforms.Resize(IMAGE_RESIZE),
        transforms.RandomResizedCrop(IMAGE_CROP, scale=(0.8, 1.0)),
        transforms.RandomHorizontalFlip(),
        transforms.RandomVerticalFlip(),
        transforms.ColorJitter(brightness=0.2, contrast=0.2, saturation=0.2, hue=0.05),
        transforms.ToTensor(),
        transforms.Normalize(mean=NORM_MEAN, std=NORM_STD),
    ])


def get_pretrain_transforms():
    """Aggressive augmentation for self-supervised pretraining (Stage 4).
    Masking is handled separately in the pretraining loop."""
    return transforms.Compose([
        transforms.Resize(IMAGE_RESIZE),
        transforms.RandomResizedCrop(IMAGE_CROP, scale=(0.5, 1.0)),
        transforms.RandomHorizontalFlip(),
        transforms.RandomVerticalFlip(),
        transforms.ColorJitter(brightness=0.4, contrast=0.4, saturation=0.3, hue=0.05),
        transforms.RandomGrayscale(p=0.1),
        transforms.GaussianBlur(kernel_size=5, sigma=(0.1, 2.0)),
        transforms.ToTensor(),
        transforms.Normalize(mean=NORM_MEAN, std=NORM_STD),
    ])
